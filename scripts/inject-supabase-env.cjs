/**
 * Netlify build step: write js/supabase-env.js from environment variables.
 * Each Netlify site (dev / staging / prod) sets its own SUPABASE_URL + publishable key.
 *
 * Env (first non-empty wins for the key):
 *   SUPABASE_PUBLISHABLE_KEY
 *   SUPABASE_ANON_KEY
 *   VITE_SUPABASE_ANON_KEY
 */

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const outPath = path.join(root, 'js', 'supabase-env.js');

const url =
  process.env.SUPABASE_URL ||
  process.env.VITE_SUPABASE_URL ||
  process.env.REACT_APP_SUPABASE_URL ||
  '';

const publishableKey =
  process.env.SUPABASE_PUBLISHABLE_KEY ||
  process.env.SUPABASE_ANON_KEY ||
  process.env.VITE_SUPABASE_ANON_KEY ||
  '';

const lines = [
  '/**',
  ' * Deploy-time Supabase browser config (publishable key only — safe in the browser).',
  ' * Generated by scripts/inject-supabase-env.cjs during Netlify build.',
  ' * Do not edit by hand; set Netlify env vars instead.',
  ' */',
  '(function () {',
  "  if (typeof window === 'undefined') return;",
  '  window.__SUPABASE_CONFIG__ = Object.assign({}, window.__SUPABASE_CONFIG__ || {}, {'
];

if (url) {
  lines.push(`    url: ${JSON.stringify(url)},`);
}
if (publishableKey) {
  lines.push(`    anonKey: ${JSON.stringify(publishableKey)},`);
}

lines.push('  });');
lines.push('})();');
lines.push('');

fs.writeFileSync(outPath, lines.join('\n'), 'utf8');

if (!url || !publishableKey) {
  console.warn(
    '[inject-supabase-env] Missing SUPABASE_URL and/or SUPABASE_PUBLISHABLE_KEY —',
    'js/supabase-env.js written with partial/empty config.',
    'Browser will fall back to defaults in js/supabase-client.js (legacy anon).'
  );
} else {
  const keyLabel = publishableKey.startsWith('sb_publishable_')
    ? 'publishable'
    : publishableKey.startsWith('eyJ')
      ? 'legacy anon JWT'
      : 'custom';
  console.log(`[inject-supabase-env] Wrote js/supabase-env.js (${keyLabel} key, url set).`);
}
